document.querySelector("#quantidade-mais10").addEventListener("click", function(){
  console.log("mais")
  let $quantidade = document.querySelector("#quantidade10")
  $quantidade.textContent = Number($quantidade.textContent) + 1
})

document.querySelector("#quantidade-menos10").addEventListener("click", function(){
  console.log("menos")
  let $quantidade = document.querySelector("#quantidade10")
  let quantidadeNumber = Number($quantidade.textContent)
  if (quantidadeNumber > 0 ) {
    $quantidade.textContent = quantidadeNumber - 1
  }
  
})

